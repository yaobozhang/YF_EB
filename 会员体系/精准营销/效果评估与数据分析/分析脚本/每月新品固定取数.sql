		PURC_CLAS_LEV2_NAME,  --采购分类二级名称
		PURC_CLAS_LEV3_CODE,  --采购分类三级编码
		PURC_CLAS_LEV3_NAME, --采购分类三级编码
		STORE_QUATITY
from EXT_TMP.GOODS_CHOOSE_V1_0)b
on a.goods_code=b.goods_code
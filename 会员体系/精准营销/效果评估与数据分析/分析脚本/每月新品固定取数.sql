SELECT A.GOODS_CODE,GOODS_NAME,
		PURC_CLAS_LEV1_CODE,
		PURC_CLAS_LEV1_NAME,  --采购分类一级名称
		PURC_CLAS_LEV2_CODE,  --采购分类二级编码
		PURC_CLAS_LEV2_NAME,  --采购分类二级名称
		PURC_CLAS_LEV3_CODE,  --采购分类三级编码
		PURC_CLAS_LEV3_NAME, --采购分类三级编码
